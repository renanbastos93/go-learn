package main

import "fmt"

func main() {
	var a int
	var b float64
	var c bool
	var d string
	var e *int

	fmt.Println("Default value to type int ", a)
	fmt.Println("Default value to type float64 ", b)
	fmt.Println("Default value to type bool ", c)
	fmt.Println("Default value to type string ", d)
	fmt.Println("Default value to type *int ", e)
}